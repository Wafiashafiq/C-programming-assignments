#include <stdio.h>

int main() {
    int x = 10;
    int y = 5;

    int sum = x + y;
    printf("%d\n", sum);

    int difference = x - y;
    printf("%d\n", difference);

    int product = x * y;
    printf("%d\n", product);

    int quotient = x / y;
    printf("%d\n", quotient);

    return 0;
}